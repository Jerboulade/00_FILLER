#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int a;

	a = ft_strlen(s1);
	while (*s2)
	{
		s1[a] = *s2;
		a++;
		s2++;
	}
	s1[a] = 0;
	return (s1);
}
